class CovidInfo {
  final String? country;
  final int? totalConfirmed;
  final int? totalDeaths;
  final int? totalRecovered;
  final int? newConfirmed;
  final int? newDeaths;
  final int? newRecovered;
  final String? dateTime;

  CovidInfo({
    this.country,
    this.totalConfirmed,
    this.totalDeaths,
    this.totalRecovered,
    this.newConfirmed,
    this.newDeaths,
    this.newRecovered,
    this.dateTime,
  });
}
